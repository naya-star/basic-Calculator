# Assigning multiple variables in one line
a, b, c = 5, 7, "Hello world"

# Prints the value of 'a'
print(a)  # prints 5

# Prints the value of 'b'
print(b)  # prints 7 (corrected from 5)

# Prints the value of 'c'
print(c)  # prints Hello world

# Assigning integer values to variables
num = 55
Num = 510

# Prints the value of 'num'
print(num)  # prints 55 (corrected from 5)

# Prints the value of 'Num'
print(Num)  # prints 510

# Assigning integer and float values
num1 = 55
num2 = 5.3

# Prints the value of 'num1'
print(num1)  # prints 55

# Prints the value of 'num2'
print(num2)  # prints 5.3

# Creating a list with mixed data types
languages = ["Python", "Dart", "Web", 23]

# Prints the entire 'languages' list
print(languages)  # prints ['Python', 'Dart', 'Web', 23]

# Accessing an element from the list by index
languages = ["Python", "Dart", "Web", 23]

# Prints the second item in the list (index 1)
print(languages[1])  # prints Dart

# Creating a tuple with mixed data types
products = ('XBox', 499.99, "Habibi", 23)

# Prints the entire 'products' tuple
print(products)  # prints ('XBox', 499.99, 'Habibi', 23)

# Accessing an element from the tuple by index
products = ('XBox', 499.99, "Habibi", 23)

# Prints the third item in the tuple (index 2)
print(products[2])  # prints Habibi

# Assigning a string value to a variable
site_name = "Power Learn Project"

# Prints the value of 'site_name'
print(site_name)  # prints Power Learn Project

# Creating a set with unique student IDs
student_ids = {112, 114, 117, 113}

# Prints the entire 'student_ids' set
print(student_ids)  # prints {112, 113, 114, 117} (order may vary)

# Creating a dictionary with country-capital pairs
capital_city = {"Kenya": "Nairobi", "Nigeria": "Lagos"}

# Prints the entire 'capital_city' dictionary
print(capital_city)  # prints {'Kenya': 'Nairobi', 'Nigeria': 'Lagos'}
